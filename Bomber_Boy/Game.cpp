void Game::brick_gen()
{	
	bool generated = false;
	int brick_x = (1 + (rand() % 11)) * 25;

	int brick_y;
	if (brick_x % 2 == 0) {
		brick_y = (1 + (rand() % 6)) * 50 - 25;
	}
	else {
		brick_y = (1 + (rand() % 11)) * 25;
	}

	if ((brick_x == 25 && brick_y == 25) || (brick_x == 25 && brick_y == 50) || (brick_x == 50 && brick_y == 25)) {
		brick_gen();
	}
	else {
		for (int j = 0; j < bricks.size(); j++) {
			if (brick_x == bricks[j].first && brick_y == bricks[j].second) {
				brick_gen();
				generated = true;
				break;
			}
		}
		if (!generated) {
			bricks.push_back(std::make_pair(brick_x, brick_y));
			place_brick(brick_x, brick_y);
		}
	}
	/*
	1. randomly generate a number (x) from 1-11 (*25)
	2. randomly generate a number (y)
	if (x % 2 == 0) generate from 1-6 (*50 - 25)
	else generate from 1-11 (*25)
	3. put a brick wall at x, y [check if x, y are 1, 1; 1, 2; 2, 1;]
	4. add the wall as a destructable obstacle in a vector
	5. check if there is another brick wall at x, y. If so run the brick generator again outside the regular loop
	4. repeat 40(?) times
	*/
}

