Game::Game()
{	
	for (int x = 0; x < turf_size * 25; x += 25) {
		for (int y = 0; y < turf_size * 25; y += 25) {
			if (x == 0 || x == (turf_size - 1) * 25 || y == 0 || y == (turf_size - 1) * 25 || (x % 2 == 0 && y % 2 == 0)) {
				obstacles.push_back(std::make_pair(x, y));
				place_solid_wall(x, y);
			}
			else {
				place_grass(x, y);
			}
		}
	}

	for (int i = 0; i < 40; i++) {
		brick_gen();
	}

	player.draw(mydc);
}

void Game::update()
{
	input();
	draw();
	player.bomb_update(mydc, obstacles, bricks);
}

